public class Box {
    double width = 2;
    double height = 5;
    double depth= 3;
    String calculateVolume() {
        return "box volume is: " + (width * height * depth);
    }

    public String depth() {
    return "box depth is: " + depth;
    }

    public String height() {
    return "box height is: " + height;
    }

    public String width() {
        return  "box width is: " + width;
    }
}
